function onResourceLoad(e) {
    var src = e.target.src;
    var found = false;
    for (var i = 0; i < blacklist.length; ++i) {
        if (src.indexOf(blacklist[i]) !== -1) {
            found = true;
            break;
        }
    }
    if (found) {
        e.target.style.display = "none !important";
    }
};
window.onload = function() {
    var tags = ["img", "video"];
    var elems = document.querySelectorAll(tags.join(" "));
    for (var i = 0; i < elems.length; ++i) {
        elems[i].addEventListener("load", onResourceLoad);
    }
};